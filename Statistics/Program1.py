# Mean and Standard Deviation Calculation

import numpy as np

data = [12, 15, 18, 20, 22]
mean = np.mean(data)
std_dev = np.std(data)

print("Data:", data)

# Output::Data: [12, 15, 18, 20, 22]

print("Mean:", mean)

# Output::Mean: 17.4

print("Standard Deviation:", std_dev)

# Output::Standard Deviation: 3.5552777669262356
